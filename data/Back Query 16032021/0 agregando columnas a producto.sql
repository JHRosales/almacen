--AGREGANDO COLUMNA FECHA EN LA TABLA producto
IF NOT EXISTS(
			SELECT *
			FROM INFORMATION_SCHEMA.COLUMNS
			WHERE TABLE_NAME = N'producto' AND COLUMN_NAME='nStockMinimo')
		BEGIN
			ALTER TABLE producto 
			ADD nStockMinimo int NULL
		END
		
